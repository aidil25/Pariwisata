package org.aidil.model;

/**
 *
 * @author Aidil
 */
public class Guide {
    String id_guide,nama_guide,alamat,telepon,tarif_guide;

    public String getId_guide() {
        return id_guide;
    }

    public void setId_guide(String id_guide) {
        this.id_guide = id_guide;
    }

    public String getNama_guide() {
        return nama_guide;
    }

    public void setNama_guide(String nama_guide) {
        this.nama_guide = nama_guide;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTelepon() {
        return telepon;
    }

    public void setTelepon(String telepon) {
        this.telepon = telepon;
    }

    public String getTarif_guide() {
        return tarif_guide;
    }

    public void setTarif_guide(String tarif_guide) {
        this.tarif_guide = tarif_guide;
    }
    
    
}
